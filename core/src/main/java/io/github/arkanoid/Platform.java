package io.github.arkanoid;

public class Platform {
    public float x, y;
    public float vx;
    public float width, height;



    public Platform(float x, float y, float vx, float width, float height) {
        this.x = x;
        this.y = y;
        this.vx = vx;
        this.width = width;
        this.height = height;
    }
}
